    private class Orologio implements Runnable{
        GregorianCalendar orario;
        
        public void run() {
            while(true){
                orario=new GregorianCalendar();
                int ore=orario.get(Calendar.HOUR_OF_DAY);
                int minuti=orario.get(Calendar.MINUTE);
                int secondi=orario.get(Calendar.SECOND);
                if(minuti<10 && secondi<10){
                    lbOrologio.setText(ore+":0"+minuti+":0"+secondi);  
                }else if(minuti<10){
                    lbOrologio.setText(ore+":0"+minuti+":"+secondi);   
                }else if(secondi<10){
                    lbOrologio.setText(ore+":"+minuti+":0"+secondi);
                }else{
                   lbOrologio.setText(ore+":"+minuti+":"+secondi); 
                }
            }
        }
        
    } 
    
