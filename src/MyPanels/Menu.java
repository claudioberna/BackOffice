     private void addRowToProdotto(){
         DefaultTableModel model= (DefaultTableModel) tbProdotti.getModel();
         ArrayList<Prodotto> elenco=elencoProdotti();
         Object rowData[]=new Object[2];
         for(int i=0;i<elenco.size();i++){
             rowData[0]=elenco.get(i).nomeProdotto;
             rowData[1]=elenco.get(i).quantità;
             model.addRow(rowData);
         }   
     }
     
